	public void setAutoCommit(boolean autoCommit) throws SQLException
	{
	    if (autoCommit) {
	    	if (trxName != null) {
	    		Trx trx = Trx.get(trxName, false);
	    		if (trx != null)
	    			trx.close();
	    	}
	    } else {
	    	if (trxName == null) {
	    		trxName = Trx.createTrxName();
	    	}
	    }
	}

